package com.cloudtara.core.utils;

public class Maths {

    public static long add(int numOne, int numTwo) {
        return numOne + numTwo;
    }

    public static int sub(int numOne, int numTwo) {
        return numOne - numTwo;
    }

    public static long multiply(int numOne, int numTwo) {
        return (long) numOne * numTwo;
    }

    public static double divide(int numOne, int numTwo) {
        return (double) numOne / numTwo;
    }
}
